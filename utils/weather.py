def create_weather_profile(
    temperature,
    rainfall,
    flood_risk,
    drought_risk
):


    weather = {


        "temperature":

        temperature,


        "rainfall":

        rainfall,


        "flood_risk":

        flood_risk,


        "drought_risk":

        drought_risk

    }


    return weather